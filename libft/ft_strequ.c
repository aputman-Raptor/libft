#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;

	i = 0;
	if (s1[i] == '\0' && s2[i] == '\0')
		return (1);
	while (s1[i] && s2[i] && (s1[i] == s2[i]))
	{
		if (ft_strcmp((char *)s1, (char *)s2))
			return (0);
		i++;
	}
	return (1);
}
