#include "libft.h"

int		ft_sqrt(int nb)
{
	int	value;
	int	i;

	value = 0;
	i = 0;
	while (value < nb)
	{
		i++;
		value = i * i;
	}
	if ((nb % value) != 0)
		return (0);
	else
		return (1);
}
